d = {"камень": ["ножницы", "ром"],
     "ножницы": ["бумага", "ром"],
     "ром": ["пират", "бумага"],
     "пират": ["камень", "ножницы"],
     "бумага": ["пират", "камень"]}

first = input()
second = input()

if second == first:
    print("ничья")
elif second in d[first]:
    print("первый")
else:
    print("второй")
